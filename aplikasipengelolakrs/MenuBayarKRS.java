    private void btnInputBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputBayarActionPerformed
        int bayar = Integer.parseInt(txtBayar.getText());
        
        if(txtNpm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data NPM Belum Dipilih!!");
        } else if (bayar < 800000) {
            JOptionPane.showMessageDialog(null, "Total Yang Harus Dibayar Tidak Boleh Kurang Dari Rp800.000!!"); 
        } else {
            dtm = (DefaultTableModel)tableRiwayat.getModel();
            ArrayList data = new ArrayList();
            data.add(txtNpm.getText());
            data.add(txtNama.getText());
            data.add(txtSemester.getText());
            data.add(tipeBayar.getSelectedItem().toString());
            data.add(txtBayar.getText());
            dtm.addRow(data.toArray());
            cleanTxt();
        }
    }//GEN-LAST:event_btnInputBayarActionPerformed

    private void btnSimpanDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanDataActionPerformed
        int bayar = Integer.parseInt(txtBayar.getText());
        
        if(txtNpm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data NPM Belum Dipilih!!");
        } else if (bayar < 800000) {
            JOptionPane.showMessageDialog(null, "Total Yang Harus Dibayar Tidak Boleh Kurang Dari Rp800.000!!"); 
        } else {
            try {
                con.setAutoCommit(false);
                String dbTambah = "INSERT INTO bayar VALUES (null, ?, ?, ?, ?, ?);";
                pst = con.prepareStatement(dbTambah);
                pst.setString(1, txtNpm.getText());
                pst.setString(2, txtNama.getText());
                pst.setString(3, txtSemester.getText());
                pst.setString(4, tipeBayar.getSelectedItem().toString());
                pst.setString(5, txtBayar.getText());
                pst.executeUpdate();
                
                con.commit();
                JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan!!", "Sukses Menambahkan Data", 1);
                dtm.setRowCount(0);
                cleanTxt();
            } catch (SQLException ex) {
                Logger.getLogger(MenuBayarKRS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSimpanDataActionPerformed

