    private void txtBayarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBayarKeyTyped
        char c = evt.getKeyChar();
                
        if (!Character.isDigit(c)) {
            evt.consume();
        }
        
        if (txtBayar.getText().length() >= 12) {
            JOptionPane.showMessageDialog(null, "Angka Yang Dimasukkan Harus tidak lebih dari 12!!", "Peringatan", 2);
            evt.consume();
        }
    }//GEN-LAST:event_txtBayarKeyTyped

    private void tableRiwayatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRiwayatMouseClicked
        int row = tableRiwayat.getSelectedRow();
        
        txtNpm.setText(tableRiwayat.getValueAt(row, 0).toString());
        txtNama.setText(tableRiwayat.getValueAt(row, 1).toString());
        txtSemester.setText(tableRiwayat.getValueAt(row, 2).toString());
        tipeBayar.setSelectedItem(tableRiwayat.getValueAt(row, 3).toString());
        txtBayar.setText(tableRiwayat.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tableRiwayatMouseClicked

