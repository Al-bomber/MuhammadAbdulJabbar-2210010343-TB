    private void btnHapusKRSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusKRSActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null,"Hapus Data KRS Ini?", "Konfirmasi Hapus", 0);
                
            if (confirm == 0) { // untuk jika memilih ya saat joption muncul akan menghapus datanya
                try {
                    String queryHapus = "DELETE FROM krs WHERE id = ?;";
                    pst = con.prepareStatement(queryHapus);
                    pst.setString(1, id);
                    pst.executeUpdate();
                        
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!!", "Sukses Menghapus", 1);
                    showData();
                } catch (SQLException ex) {
                    Logger.getLogger(MenuCekKRS.class.getName()).log(Level.SEVERE, null, ex);
                }            
            }
    }//GEN-LAST:event_btnHapusKRSActionPerformed

