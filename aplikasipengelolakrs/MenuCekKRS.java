    private void btnCetakKRSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakKRSActionPerformed
        int row = tableKRS.getSelectedRow();
        id = tableKRS.getValueAt(row, 0).toString();
        
        StringBuilder content = new StringBuilder();
        content.append("Data KRS Universitas Teknologi Kalsel Anda :").append("\n\n");
            
        content.append("Nama : ").append(tableKRS.getValueAt(row, 1).toString()).append("\n");
        content.append("NPM : ").append(tableKRS.getValueAt(row, 2).toString()).append("\n");
        content.append("Semester : ").append(tableKRS.getValueAt(row, 3).toString()).append("\n");
        content.append("Kelas : ").append(tableKRS.getValueAt(row, 4).toString()).append("\n");
        content.append("Mata Kuliah 1 : ").append(tableKRS.getValueAt(row, 5).toString()).append("\n");
        content.append("Mata Kuliah 2 : ").append(tableKRS.getValueAt(row, 6).toString()).append("\n");
        content.append("Mata Kuliah 3 : ").append(tableKRS.getValueAt(row, 7).toString()).append("\n");
        content.append("Mata Kuliah 4 : ").append(tableKRS.getValueAt(row, 8).toString()).append("\n");
        content.append("Mata Kuliah 5 : ").append(tableKRS.getValueAt(row, 9).toString()).append("\n");
        content.append("Mata Kuliah 6 : ").append(tableKRS.getValueAt(row, 10).toString()).append("\n");
            
            
            
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan Data Alamat");
        int userSelection = fileChooser.showSaveDialog(this);
            
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            try (FileWriter fileWriter = new FileWriter(fileChooser.getSelectedFile() + ".txt")) {
                fileWriter.write(content.toString());
                JOptionPane.showMessageDialog(this, "Data kontak berhasil disimpan ke Perangkat");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan file: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCetakKRSActionPerformed

