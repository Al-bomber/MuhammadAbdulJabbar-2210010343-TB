    private void btnCetakBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakBayarActionPerformed
        int row = tableBayar.getSelectedRow();
        id = tableBayar.getValueAt(row, 0).toString();
        
        StringBuilder content = new StringBuilder();
        content.append("Berikut Data Riwayat Pembayaran KRS Anda :").append("\n\n");
            
        content.append("Nama : ").append(tableBayar.getValueAt(row, 1).toString()).append("\n");
        content.append("NPM : ").append(tableBayar.getValueAt(row, 2).toString()).append("\n");
        content.append("Semester : ").append(tableBayar.getValueAt(row, 3).toString()).append("\n");
        content.append("Tipe Pembayaran : ").append(tableBayar.getValueAt(row, 4).toString()).append("\n");
        content.append("Total Bayar : Rp").append(tableBayar.getValueAt(row, 5).toString()).append("\n");
            
            
            
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan Data Alamat");
        int userSelection = fileChooser.showSaveDialog(this);
            
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            try (FileWriter fileWriter = new FileWriter(fileChooser.getSelectedFile() + ".txt")) {
                fileWriter.write(content.toString());
                JOptionPane.showMessageDialog(this, "Data kontak berhasil disimpan ke Perangkat");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan file: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCetakBayarActionPerformed

