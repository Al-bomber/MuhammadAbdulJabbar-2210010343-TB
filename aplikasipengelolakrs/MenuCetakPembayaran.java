    private void showData() {
        try {
            String[] judul = {"ID", "Nama", "NPM", "Semester", "Tipe Pembayaran", "Total Bayar"};
            DefaultTableModel dtm = new DefaultTableModel(null, judul);
            tableBayar.setModel(dtm);
            
            String sql = "SELECT * FROM bayar";
            
            if (!txtCariData.getText().isEmpty()) {
                    sql = "SELECT * FROM bayar WHERE nama LIKE '%" + txtCariData.getText() + "%';";
            }
            
            pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MenuCekKRS.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
